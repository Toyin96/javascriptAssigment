function checkIntegers(arg1, arg2){
    if (arg1 >= 0){
        console.log(`${arg1} is a positive integer`);
    }else{
        console.log(`${arg1} is a negative integer`);
    }

    if (arg2 >= 0){
        console.log(`${arg2} is a positive integer`);
    }else{
        console.log(`${arg2} is a negative integer`);
    }
}

checkIntegers(-2, -2);